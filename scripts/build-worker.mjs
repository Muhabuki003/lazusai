// Bundles admin/dashboard.html + src/worker.js into dist/_worker.js for
// Cloudflare Pages deployment. Run before deploy (npm run deploy does both).
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const html = readFileSync(join(root, "admin", "dashboard.html"), "utf8");
const workerSrc = readFileSync(join(root, "src", "worker.js"), "utf8");

// Inline the dashboard HTML as a constant at the top of the worker bundle.
const inlinedHtml = `// AUTO-GENERATED by scripts/build-worker.mjs — do not edit by hand.
export const ADMIN_HTML = ${JSON.stringify(html)};
`;

// Replace the import line with the inlined constant so the output is a single
// self-contained file that Cloudflare Pages can deploy as _worker.js.
const bundle = workerSrc.replace(
  /^import\s*\{[^}]*ADMIN_HTML[^}]*\}\s*from\s*['"]\.\/admin-html\.generated\.js['"];?\s*\n?/m,
  inlinedHtml + "\n",
);

mkdirSync(join(root, "dist"), { recursive: true });
writeFileSync(join(root, "dist", "_worker.js"), bundle);

console.log(`Built dist/_worker.js (${bundle.length} bytes) — ready for wrangler pages deploy`);
