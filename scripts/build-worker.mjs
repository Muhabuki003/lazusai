// Bundles admin/dashboard.html + src/worker.js into dist/_worker.js for
// Cloudflare Pages deployment, and copies landing/index.html as a static
// front-end page into dist/ so the root URL serves the marketing site.
// Run before deploy (npm run deploy does both).
import { readFileSync, writeFileSync, mkdirSync, copyFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const dist = join(root, "dist");
mkdirSync(dist, { recursive: true });

// 1. Build the worker bundle (inlines dashboard HTML)
const html = readFileSync(join(root, "admin", "dashboard.html"), "utf8");
const workerSrc = readFileSync(join(root, "src", "worker.js"), "utf8");

const inlinedHtml = `// AUTO-GENERATED by scripts/build-worker.mjs — do not edit by hand.
export const ADMIN_HTML = ${JSON.stringify(html)};
`;

const bundle = workerSrc.replace(
  /^import\s*\{[^}]*ADMIN_HTML[^}]*\}\s*from\s*['"]\.\/admin-html\.generated\.js['"];?\s*\n?/m,
  inlinedHtml + "\n",
);

writeFileSync(join(dist, "_worker.js"), bundle);

// 2. Copy the landing page as a static asset
const landingSrc = join(root, "landing", "index.html");
try {
  copyFileSync(landingSrc, join(dist, "index.html"));
  console.log(`Copied landing/index.html → dist/index.html`);
} catch (e) {
  console.warn(`⚠️  landing/index.html not found, skipping static landing page`);
}

console.log(`Built dist/_worker.js (${bundle.length} bytes) — ready for wrangler pages deploy`);
